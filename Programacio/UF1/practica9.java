import java.io.*;

public class practica9{
	static final int MAX_TABLA=100;
	public static void main(String[] args) throws IOException {

	int i;
	int tabla[];
	tabla=new int [MAX_TABLA];
	for (i=0;i<MAX_TABLA;i++){
		tabla[i]=(i+1)*7;
	}
	for (i=0;i<10;i++){
		System.out.print(tabla[i]+",");
		}
		System.out.println("\n");
	for (i=9;i>4;i--){
		tabla[1+i]=tabla[i];
		}
	tabla[5]=100;
	for (i=0;i<11;i++){
		System.out.print(tabla[i]+",");
		}



	}

}
